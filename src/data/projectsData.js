const buildShots = (url) => {
  if (!url) {
    return []
  }

  const encodedUrl = encodeURIComponent(url)
  const mShotLarge = `https://s.wordpress.com/mshots/v1/${encodedUrl}?w=1600`
  const mShotMedium = `https://s.wordpress.com/mshots/v1/${encodedUrl}?w=1200`
  const thumFallback = `https://image.thum.io/get/width/1200/noanimate/${url}`

  return [mShotLarge, mShotMedium, thumFallback]
}

